    <script>
        const hamburger = document.querySelector('.hamburger');
        const navLinks = document.querySelector('.nav-links');
        const navbar = document.querySelector('.navbar');
        const scrollLinks = document.querySelectorAll('.scroll-link');
        const filterButtons = document.querySelectorAll('.filter-tabs button');
        const projectCards = document.querySelectorAll('.project-card');
        const scrollTopBtn = document.querySelector('.scroll-top');

        // Certificate Carousel (ชุดแนวตั้ง)
        const carousel = document.querySelector('.certificate-carousel');
        const container = carousel.querySelector('.carousel-container');
        const slide = carousel.querySelector('.carousel-slide');
        const images = carousel.querySelectorAll('.certificate-img');
        const prevBtn = carousel.querySelector('.carousel-prev');
        const nextBtn = carousel.querySelector('.carousel-next');
        const dots = carousel.querySelectorAll('.dot');
        let currentIndex = 0;
        const totalOriginalSlides = images.length;
        const totalSlides = totalOriginalSlides + 2;

        const firstClone = images[0].cloneNode(true);
        const lastClone = images[totalOriginalSlides - 1].cloneNode(true);
        slide.appendChild(firstClone);
        slide.insertBefore(lastClone, images[0]);

        const allImages = slide.querySelectorAll('.certificate-img');
        allImages[1].classList.add('selected');

        function updateSlide() {
            const slideWidth = container.offsetWidth / 3;
            const slideOffset = -((currentIndex + 1) * slideWidth) + (container.offsetWidth / 2 - slideWidth / 2);
            slide.style.transition = 'transform 0.5s ease-in-out';
            slide.style.transform = `translateX(${slideOffset}px)`;

            const dotIndex = currentIndex >= totalOriginalSlides ? 0 : (currentIndex < 0 ? totalOriginalSlides - 1 : currentIndex);
            dots.forEach((dot, index) => {
                dot.classList.toggle('active', index === dotIndex);
            });

            allImages.forEach((img, index) => {
                img.classList.toggle('selected', index === currentIndex + 1);
            });

            if (currentIndex >= totalOriginalSlides) {
                setTimeout(() => {
                    slide.style.transition = 'none';
                    currentIndex = 0;
                    const newSlideOffset = -((currentIndex + 1) * slideWidth) + (container.offsetWidth / 2 - slideWidth / 2);
                    slide.style.transform = `translateX(${newSlideOffset}px)`;
                    allImages.forEach((img, index) => {
                        img.classList.toggle('selected', index === currentIndex + 1);
                    });
                }, 500);
            } else if (currentIndex < 0) {
                setTimeout(() => {
                    slide.style.transition = 'none';
                    currentIndex = totalOriginalSlides - 1;
                    const newSlideOffset = -((currentIndex + 1) * slideWidth) + (container.offsetWidth / 2 - slideWidth / 2);
                    slide.style.transform = `translateX(${newSlideOffset}px)`;
                    allImages.forEach((img, index) => {
                        img.classList.toggle('selected', index === currentIndex + 1);
                    });
                }, 500);
            }
        }

        function slideLeft() {
            currentIndex++;
            updateSlide();
        }

        function slideRight() {
            currentIndex--;
            updateSlide();
        }

        if (prevBtn && nextBtn) {
            prevBtn.addEventListener('click', slideRight);
            nextBtn.addEventListener('click', slideLeft);
        }

        window.addEventListener('resize', updateSlide);
        updateSlide();

        // Certificate Carousel (ชุดแนวนอน)
        const carouselHorizontal = document.querySelector('.certificate-carousel-horizontal');
        const containerHorizontal = carouselHorizontal.querySelector('.carousel-container');
        const slideHorizontal = carouselHorizontal.querySelector('.carousel-slide');
        const imagesHorizontal = carouselHorizontal.querySelectorAll('.certificate-img');
        const prevBtnHorizontal = carouselHorizontal.querySelector('.carousel-prev');
        const nextBtnHorizontal = carouselHorizontal.querySelector('.carousel-next');
        const dotsHorizontal = carouselHorizontal.querySelectorAll('.dot');
        let currentIndexHorizontal = 0;
        const totalOriginalSlidesHorizontal = imagesHorizontal.length;
        const totalSlidesHorizontal = totalOriginalSlidesHorizontal + 2;

        const firstCloneHorizontal = imagesHorizontal[0].cloneNode(true);
        const lastCloneHorizontal = imagesHorizontal[totalOriginalSlidesHorizontal - 1].cloneNode(true);
        slideHorizontal.appendChild(firstCloneHorizontal);
        slideHorizontal.insertBefore(lastCloneHorizontal, imagesHorizontal[0]);

        const allImagesHorizontal = slideHorizontal.querySelectorAll('.certificate-img');
        allImagesHorizontal[1].classList.add('selected');

        function updateSlideHorizontal() {
            const slideWidth = containerHorizontal.offsetWidth / 3;
            const slideOffset = -((currentIndexHorizontal + 1) * slideWidth) + (containerHorizontal.offsetWidth / 2 - slideWidth / 2);
            slideHorizontal.style.transition = 'transform 0.5s ease-in-out';
            slideHorizontal.style.transform = `translateX(${slideOffset}px)`;

            const dotIndex = currentIndexHorizontal >= totalOriginalSlidesHorizontal ? 0 : (currentIndexHorizontal < 0 ? totalOriginalSlidesHorizontal - 1 : currentIndexHorizontal);
            dotsHorizontal.forEach((dot, index) => {
                dot.classList.toggle('active', index === dotIndex);
            });

            allImagesHorizontal.forEach((img, index) => {
                img.classList.toggle('selected', index === currentIndexHorizontal + 1);
            });

            if (currentIndexHorizontal >= totalOriginalSlidesHorizontal) {
                setTimeout(() => {
                    slideHorizontal.style.transition = 'none';
                    currentIndexHorizontal = 0;
                    const newSlideOffset = -((currentIndexHorizontal + 1) * slideWidth) + (containerHorizontal.offsetWidth / 2 - slideWidth / 2);
                    slideHorizontal.style.transform = `translateX(${newSlideOffset}px)`;
                    allImagesHorizontal.forEach((img, index) => {
                        img.classList.toggle('selected', index === currentIndexHorizontal + 1);
                    });
                }, 500);
            } else if (currentIndexHorizontal < 0) {
                setTimeout(() => {
                    slideHorizontal.style.transition = 'none';
                    currentIndexHorizontal = totalOriginalSlidesHorizontal - 1;
                    const newSlideOffset = -((currentIndexHorizontal + 1) * slideWidth) + (containerHorizontal.offsetWidth / 2 - slideWidth / 2);
                    slideHorizontal.style.transform = `translateX(${newSlideOffset}px)`;
                    allImagesHorizontal.forEach((img, index) => {
                        img.classList.toggle('selected', index === currentIndexHorizontal + 1);
                    });
                }, 500);
            }
        }

        function slideLeftHorizontal() {
            currentIndexHorizontal++;
            updateSlideHorizontal();
        }

        function slideRightHorizontal() {
            currentIndexHorizontal--;
            updateSlideHorizontal();
        }

        if (prevBtnHorizontal && nextBtnHorizontal) {
            prevBtnHorizontal.addEventListener('click', slideRightHorizontal);
            nextBtnHorizontal.addEventListener('click', slideLeftHorizontal);
        }

        window.addEventListener('resize', updateSlideHorizontal);
        updateSlideHorizontal();

        // Modal for Certificate Images
        const modal = document.getElementById('certificateModal');
        const modalImg = document.getElementById('modalImage');
        const closeModal = document.querySelector('.modal-close');
        const certificateImgs = document.querySelectorAll('.certificate-img');

        certificateImgs.forEach(img => {
            img.addEventListener('click', () => {
                modal.style.display = 'flex';
                modalImg.src = img.src;
            });
        });

        if (closeModal) {
            closeModal.addEventListener('click', () => {
                modal.style.display = 'none';
            });
        }

        if (modal) {
            modal.addEventListener('click', (e) => {
                if (e.target === modal) {
                    modal.style.display = 'none';
                }
            });
        }

        // Hamburger Menu
        hamburger.addEventListener('click', () => {
            navLinks.classList.toggle('active');
            hamburger.classList.toggle('open');
        });

        // Smooth Scroll
        scrollLinks.forEach(link => {
            link.addEventListener('click', (event) => {
                event.preventDefault();
                const targetId = link.getAttribute('href').substring(1);
                const targetElement = document.getElementById(targetId);
                if (targetElement) {
                    const navbarHeight = navbar.offsetHeight;
                    const targetPosition = targetElement.getBoundingClientRect().top + window.pageYOffset;
                    const offsetPosition = targetPosition - navbarHeight;
                    window.scrollTo({
                        top: offsetPosition,
                        behavior: 'smooth'
                    });
                }
                if (navLinks.classList.contains('active')) {
                    setTimeout(() => {
                        navLinks.classList.remove('active');
                        hamburger.classList.remove('open');
                    }, 100);
                }
            });
        });

        // Filter Projects
        filterButtons.forEach(button => {
            button.addEventListener('click', () => {
                filterButtons.forEach(btn => btn.classList.remove('active'));
                button.classList.add('active');
                const filter = button.getAttribute('data-filter');
                projectCards.forEach(card => {
                    const category = card.getAttribute('data-category');
                    if (filter === 'all' || category === filter) {
                        card.classList.remove('hidden');
                    } else {
                        card.classList.add('hidden');
                    }
                });
            });
        });

        // Scroll Effects
        window.addEventListener('scroll', () => {
            if (window.scrollY > 50) {
                navbar.classList.add('scrolled');
            } else {
                navbar.classList.remove('scrolled');
            }
            if (window.scrollY > 100) {
                scrollTopBtn.classList.add('visible');
            } else {
                scrollTopBtn.classList.remove('visible');
            }
        });

        scrollTopBtn.addEventListener('click', () => {
            window.scrollTo({ top: 0, behavior: 'smooth' });
        });

        // Handle query parameters for contact form feedback
        window.onload = function() {
            const urlParams = new URLSearchParams(window.location.search);
            const status = urlParams.get('status');
            const message = urlParams.get('message');
            const messageDiv = document.getElementById('contact-message');

            if (status && message) {
                messageDiv.className = status === 'success' ? 'success-message' : 'error-message';
                messageDiv.innerHTML = decodeURIComponent(message);
                // Clear query parameters from URL
                window.history.replaceState({}, document.title, window.location.pathname + '#contact');
                // Optionally clear message after a few seconds
                setTimeout(() => {
                    messageDiv.innerHTML = '';
                }, 5000);
            }
        };
